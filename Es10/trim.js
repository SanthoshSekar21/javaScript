//trimStart() (also known as trimLeft()) removes whitespace from the beginning of a string.
const str = '   Hello, World!';
const trimmedStr = str.trimStart();

console.log(trimmedStr); // Outputs: 'Hello, World!'
//trimEnd() (also known as trimRight()) removes whitespace from the end of a string.
const str2 = 'Hello, World!   ';
const trimmedStr2 = str.trimEnd();

console.log(trimmedStr2); // Outputs: 'Hello, World!'
