// Function declaration
function greet(name) {
    return `Hello, ${name}!`;
}

// Calling the function
console.log(greet('santhosh'));  

// Function expression
const square = function(number) {
    return number * number;
};

// Calling the function
console.log(square(4));  

// Arrow function
const multiply = (a, b) => a * b;

// Calling the function
console.log(multiply(3, 5));  
